import {useEffect, useState} from "react";
import Head from "next/head";
import Image from "next/image";
import styles from "../styles/Gallery.module.css";
import AWS from "aws-sdk";

const s3 = new AWS.S3({
    accessKeyId: process.env.NEXT_PUBLIC_AWS_ACCESS_KEY_ID,
    secretAccessKey: process.env.NEXT_PUBLIC_AWS_SECRET_ACCESS_KEY,
    region: process.env.NEXT_PUBLIC_AWS_REGION,
});

export default function Gallery() {
    const [images, setImages] = useState([]);

    useEffect(() => {
        const getImages = async () => {
            try {
                const response = await s3
                    .listObjectsV2({
                        Bucket: process.env.NEXT_PUBLIC_AWS_BUCKET_NAME,
                    })
                    .promise();

                const imageKeys = response.Contents.map((obj) => obj.Key);
                setImages(imageKeys);
            } catch (error) {
                console.log(error);
            }
        };

        getImages();
    }, []);

    return (
        <div className={styles.container}>
            <Head>
                <title>Gallery | Next</title>
                <meta name="description" content="Generated by create next app"/>
                <link rel="icon" href="/favicon.ico"/>
            </Head>

            <main className={styles.main}>
                <h1 className={styles.title}>Gallery</h1>

                <div className={styles.grid}>
                    {images.map((imageKey) => (
                        <div key={imageKey} className={styles.card}>
                            <Image
                                src={`https://${process.env.NEXT_PUBLIC_AWS_BUCKET_NAME}.s3.${process.env.NEXT_PUBLIC_AWS_REGION}.amazonaws.com/${imageKey}`}
                                alt={imageKey}
                                width={300}
                                height={200}
                            />
                        </div>
                    ))}
                </div>
            </main>
        </div>
    );
}
